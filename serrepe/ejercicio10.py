import os
# Decodificar mensaje encriptado
# k = corre
# l = por
# m = tu
# n = vida
msg=os.sys.argv[1]
#for
for letra in msg:
    if letra == "k":
        print("corre")
    if letra == "l":
        print("por")
    if letra == "m":
        print("tu")
    if letra == "n":
        print("vida")
#fin_iterador

print("Fin del bucle")
