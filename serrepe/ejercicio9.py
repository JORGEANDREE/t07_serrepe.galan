import os
# Decodificar mensaje encriptado
# E = viva
# F = el
# G = peru
# H = !
msg=os.sys.argv[1]
#for
for letra in msg:
    if letra == "E":
        print("viva")
    if letra == "F":
        print("el")
    if letra == "G":
        print("peru")
    if letra == "H":
        print("!")
#fin_iterador

print("Fin del bucle")
