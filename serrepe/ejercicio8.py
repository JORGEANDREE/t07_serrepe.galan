import os
# Decodificar mensaje encriptado
# T = LA
# Y = PROGRAMACION
# U = ES LO
# P= MEJOR
msg=os.sys.argv[1]
#for
for letra in msg:
    if letra == "T":
        print("LA")
    if letra == "Y":
        print("PROGRAMACION")
    if letra == "U":
        print("ES LO")
    if letra == "P":
        print("MEJOR")
#fin_iterador

print("Fin del bucle")
